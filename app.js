let Me = 19;
let Brother = 24;

let isMeOlderThanBrother = Me > Brother;
let isMeYoungerThanBrother = Me < Brother;
let isMeSameAgeAsBrother = Me == Brother;
let isMeNotSameAgeAsBrother = Me !== Brother;

console.log('Am I older than my brother?', isMeOlderThanBrother);
console.log('Am I younger than my brother?', isMeYoungerThanBrother);
console.log('Am I same age as my brother?', isMeSameAgeAsBrother);
console.log('Am I not the same age as my brother?', isMeNotSameAgeAsBrother);